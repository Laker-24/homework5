using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Homework5.Data;
using Homework5.Models;

namespace Homework5.Controllers
{
    public class CartsController : Controller
    {
        private readonly Homework5Context _context;

        public CartsController(Homework5Context context)
        {
            _context = context;
        }
        public async Task<IActionResult> Index(int ? id, int low=0, int high=1000)
        {
            if (_context.Cart == null) { return Problem("Entity set 'Homework5Context.Product'  is null."); }
            if (id == null)
            {
                var Carts = from m in _context.Cart
                            where Convert.ToDecimal(low) <= m.Price
                            && m.Price <= Convert.ToDecimal(high)
                            select m;
                return View(await Carts.ToListAsync()); 
            }
            else
            {
                var Carts = from m in _context.Cart where m.ProductId == id select m;
                return View(await Carts.ToListAsync());
            }
               
